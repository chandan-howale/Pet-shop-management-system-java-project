    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtnActionPerformed
        // TODO add your handling code here:
    String adminPassword = "admin";
    if (PasswordTb.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Enter Admin Password!!");
    } else if (!PasswordTb.getText().equals(adminPassword)) {
        JOptionPane.showMessageDialog(this, "Incorrect Password. Please enter correct password!!");
    } else {
        Login.setUserRole("admin"); // Set the role after a successful admin login
        new Users().setVisible(true);
        this.dispose();
    }
    }//GEN-LAST:event_LoginBtnActionPerformed

